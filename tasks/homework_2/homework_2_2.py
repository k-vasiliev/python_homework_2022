# Считайте ввод и распечатайте первую фразу с места, где заканчивается вторая
# пример:
# пользователь ввел "Hello, welcome"
# пользователь ввел "Hello"
# вывод ", welcome"

input1 = input()
input2 = input()

print(input1[len(input2):]) # удалить если в начале строки
print(input1.replace(input2, "")) # удалить из строки вообще
